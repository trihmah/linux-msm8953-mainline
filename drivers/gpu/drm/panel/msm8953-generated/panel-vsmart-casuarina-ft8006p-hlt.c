static int ft8006p_hlt_1600_prepare(struct drm_panel *panel)
{
	struct ft8006p_hlt_1600 *ctx = to_ft8006p_hlt_1600(panel);
	struct device *dev = &ctx->dsi->dev;
	int ret;

	ret = regulator_bulk_enable(ARRAY_SIZE(ft8006p_hlt_1600_supplies), ctx->supplies);
	if (ret < 0) {
		dev_err(dev, "Failed to enable regulators: %d\n", ret);
		return ret;
	}

	ft8006p_hlt_1600_reset(ctx);

	ret = ft8006p_hlt_1600_on(ctx);
	if (ret < 0) {
		dev_err(dev, "Failed to initialize panel: %d\n", ret);
		gpiod_set_value_cansleep(ctx->reset_gpio, 1);
		regulator_bulk_disable(ARRAY_SIZE(ft8006p_hlt_1600_supplies), ctx->supplies);
		return ret;
	}

	return 0;
}

static int ft8006p_hlt_1600_unprepare(struct drm_panel *panel)
{
	struct ft8006p_hlt_1600 *ctx = to_ft8006p_hlt_1600(panel);
	struct device *dev = &ctx->dsi->dev;
	int ret;

	ret = ft8006p_hlt_1600_off(ctx);
	if (ret < 0)
		dev_err(dev, "Failed to un-initialize panel: %d\n", ret);

	gpiod_set_value_cansleep(ctx->reset_gpio, 1);
	regulator_bulk_disable(ARRAY_SIZE(ft8006p_hlt_1600_supplies), ctx->supplies);

	return 0;
}

